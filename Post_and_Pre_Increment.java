package Java_Programs;

public class Post_and_Pre_Increment {
	public static void main(String a[]) 
	{
		int i = 5;
		i++;
		System.out.println(i);
		int j = 5;
		int k = j++; // Post Increment
		int L = 5;
		int M = ++L; // Pre Increment
		System.out.println(k);
		System.out.println(M);
	}

}
